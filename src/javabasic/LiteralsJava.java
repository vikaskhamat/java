package javabasic;

public class LiteralsJava {
	public static void main(String[] args) {
		//Decimal Literals (0 - 9)
		int a =1004; //base 10
		System.out.println("This is decimal literals = " + a);
		int b =0754; //Ocatal literals base 8 (0-7)
		System.out.println("This is Octaldecimal literals = " + b); // 
		
		int p=0x234; // Hexadecimal  base 16(0 -15)
		System.out.println("This is Hexadecimal literals = " + p);
		//Binaruy literlse (base 2)
		
		int bi= 0b1011;
		System.out.println(bi);//binary literals always start 0 
		
	}

}
