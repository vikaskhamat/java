package javabasic;

public class Datatype {
	public static void main(String[] args) {
		byte b =-100;
		System.out.println(b); // This is Byte Datatype range = -128 t0 127
		short s = 3450;
		System.out.println(s);// This is Short datatype
		int  i = 500; 
		System.out.println(i); //this is intger datatype
		long l = 523456234123456l; //suffix is use here
		System.out.println(l);
		float f =105.78f; // Suffix is use
		System.out.println(f);
		double d =2445.6756d;
		
		char ch ='v'; 
		System.out.println(ch);
		
		boolean bl= true;
		System.out.println(bl);
	}
	}
